#from bisect import bisect_left, bisect_right, bisect
from bisect import bisect
from typing import List
class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        
        counter_of_numbers = sum( count )
        
        minimum = next( i for i in range(256) if count[i] ) * 1.0
        maximum = next( i for i in reversed(range(256)) if count[i] ) * 1.0
        
        mean_val = sum( occurrence * num for num, occurrence in enumerate(count) ) * 1.0 / counter_of_numbers
        
        mode_val = count.index( max(count) ) * 1.0
        
        # accumulate occurrence
        for i in range(255):
            count[i+1] += count[i]
            
        median_low = bisect( count, (counter_of_numbers-1)//2 )
        median_high = bisect( count, counter_of_numbers//2 )
        median_val = ( median_low + median_high ) / 2.0
        
        return [ minimum, maximum, mean_val, median_val, mode_val ]



